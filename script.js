/* ==========================================================
   CATEGORY FILTER
========================================================== */

categoryButtons.forEach(button=>{

    button.addEventListener("click",()=>{

        categoryButtons.forEach(btn=>{

            btn.classList.remove("active");

        });

        button.classList.add("active");

        selectedCategory=button.dataset.category;

        renderMenu();

    });

});
/* ==========================================================
   GEOLOCATION
========================================================== */

const locationBtn = document.getElementById("locationBtn");
const locationStatus = document.getElementById("locationStatus");

locationBtn.addEventListener("click", () => {

    if (!navigator.geolocation) {

        locationStatus.textContent =
            "Geolocation is not supported on this device.";

        return;
    }

    locationStatus.textContent = "Getting your location...";

    navigator.geolocation.getCurrentPosition(

        (position) => {

            const lat = position.coords.latitude;
            const lng = position.coords.longitude;

            mapLink = `https://maps.google.com/?q=${lat},${lng}`;

            locationStatus.textContent =
                "✓ Current location added successfully.";

            showToast("Location added");

        },

        () => {

            mapLink = "";

            locationStatus.textContent =
                "Location permission denied. You can continue without it.";

        },

        {
            enableHighAccuracy: true,
            timeout: 15000,
            maximumAge: 0
        }

    );

});

/* ==========================================================
   WHATSAPP CHECKOUT
========================================================== */

const customerForm = document.getElementById("customerForm");

customerForm.addEventListener("submit", function (e) {

    e.preventDefault();

    if (Object.keys(cart).length === 0) {

        showToast("Your cart is empty.");

        return;
    }

    const name = document
        .getElementById("customerName")
        .value
        .trim();

    const phone = document
        .getElementById("customerPhone")
        .value
        .trim();

    const address = document
        .getElementById("customerAddress")
        .value
        .trim();

    const landmark = document
        .getElementById("customerLandmark")
        .value
        .trim();

    const notes = document
        .getElementById("customerNotes")
        .value
        .trim();

    if (!name || !phone || !address) {

        showToast("Please fill all required fields.");

        return;
    }

    if (!/^[6-9]\d{9}$/.test(phone)) {

        showToast("Enter a valid mobile number.");

        return;
    }

    let message = `Hi Cravings Destiny!%0A%0A`;

    message += `I'd like to place an order.%0A%0A`;

    message += `----------------------------%0A`;

    message += `ORDER ITEMS%0A`;

    message += `----------------------------%0A`;

    Object.keys(cart).forEach(id => {

        const item = MENU.find(x => x.id == id);

        const qty = cart[id];

        message += `• ${item.name} x ${qty} = ₹${item.price * qty}%0A`;

    });

    message += `%0A`;

    message += `Grand Total : ₹${getTotal()}%0A`;

    message += `%0A----------------------------%0A`;

    message += `CUSTOMER DETAILS%0A`;

    message += `----------------------------%0A`;

    message += `Name : ${name}%0A`;

    message += `Phone : ${phone}%0A`;

    message += `Address : ${address}%0A`;

    if (landmark) {

        message += `Landmark : ${landmark}%0A`;

    }

    if (notes) {

        message += `Notes : ${notes}%0A`;

    }

    if (mapLink) {

        message += `Location : ${mapLink}%0A`;

    }

    window.open(

        `https://wa.me/917305551983?text=${message}`,

        "_blank"

    );

});

