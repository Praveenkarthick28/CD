/* ==========================================================
   RESTORE CART
========================================================== */

function restoreCart() {

    const saved = localStorage.getItem("cd_cart");

    if (saved) {

        cart = JSON.parse(saved);

    }

}

/* ==========================================================
   INITIALIZE APPLICATION
========================================================== */

function init() {

    restoreCart();

    renderMenu();

    updateCart();

}

